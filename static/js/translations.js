// Function to get translation
function t(key, language = 'fr') {
    if (translations[language] && translations[language][key]) {
        return translations[language][key];
    }
    return translations['fr'][key] || key; // Fallback to French
}

// Function to apply translations to page
function applyTranslations(language) {
    // Update elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = t(key, language);
    });
    
    // Update elements with data-translate-placeholder attribute
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        element.placeholder = t(key, language);
    });
    
    // Update elements with data-translate-title attribute
    document.querySelectorAll('[data-translate-title]').forEach(element => {
        const key = element.getAttribute('data-translate-title');
        element.title = t(key, language);
    });
    
    // Update RTL for Arabic
    if (language === 'ar') {
        document.documentElement.dir = 'rtl';
        document.body.classList.add('rtl');
    } else {
        document.documentElement.dir = 'ltr';
        document.body.classList.remove('rtl');
    }
}

// Export for global use
window.translations = translations;
window.t = t;
window.applyTranslations = applyTranslations;
